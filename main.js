var items_per_page = 5;
var num_of_pages = generatePageNumbers();

function generatePageNumbers(){
    return Math.round(emp_data.length / items_per_page) + emp_data.length % items_per_page;
  }
var div = document.getElementById("pagelist");
for(i = 0; i <= num_of_pages+1; i++){
    var a = document.createElement("a");
    a.setAttribute("href", "#");
    if(i == 0){
        a.appendChild(document.createTextNode("\u00AB"));
        a.setAttribute("id","goleft");
        div.appendChild(a);
        }
    else if (i == num_of_pages + 1){
        a.appendChild(document.createTextNode("\u00BB"));
        a.setAttribute("id","goright");
        div.appendChild(a);
        }
    else{
        a.appendChild(document.createTextNode(i));
        a.setAttribute('id', 'page'+i);
        a.setAttribute("class","pagenumbers");
        div.appendChild(a);
        }
    }

window.onload = function(){display(1)};
var curr_page_number = 1;
document.addEventListener('click', function(event){

    if(event.target.className == "pagenumbers"){
        var pageid = event.target.id;
        pageid = pageid.replace('page','');
        document.getElementsByClassName('selectedlink')[0].style.background = "transparent";
        document.getElementsByClassName('selectedlink')[0].classList.remove('selectedlink');
        deleteTableRows();
        display(pageid);
    }
    else if((event.target.id == "goleft") && curr_page_number != 1){
        document.getElementsByClassName('selectedlink')[0].style.background = "transparent";
        document.getElementsByClassName('selectedlink')[0].classList.remove('selectedlink');
        deleteTableRows();
        display(parseInt(curr_page_number) - 1);
    }
    else if((event.target.id == "goright") && curr_page_number != num_of_pages){
        document.getElementsByClassName('selectedlink')[0].style.background = "transparent";
        document.getElementsByClassName('selectedlink')[0].classList.remove('selectedlink');
        deleteTableRows();
        display(parseInt(curr_page_number) + 1);
    }
    

},false);

function deleteTableRows(){
    var table = document.getElementById('datatable');
    b = items_per_page;
    while(b > 0){
        table.deleteRow(b);
        b--;
    }
}


function display(pagenumber){
    document.getElementById('page'+pagenumber).style.background = "red";
    document.getElementById('page'+pagenumber).classList.add('selectedlink');
    curr_page_number = pagenumber;
    var table = document.getElementById('datatable');
    var table_row_index = 1;
    for(k= items_per_page; k > 0 ; k--){
        var linedata = emp_data[items_per_page*pagenumber - k];
        var row = table.insertRow(table_row_index);
        var cell1 = row.insertCell(0);
        var cell2 = row.insertCell(1);
        var cell3 = row.insertCell(2);
        cell1.innerHTML = linedata.id;
        cell2.innerHTML = linedata.name;
        cell3.innerHTML = linedata.email;
        table_row_index++;
    }
}
